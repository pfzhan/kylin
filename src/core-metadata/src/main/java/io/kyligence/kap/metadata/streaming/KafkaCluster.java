package io.kyligence.kap.metadata.streaming;

import static org.apache.kylin.common.persistence.ResourceStore.CLUSTER_RESOURCE_ROOT;

import java.io.Serializable;
import java.util.LinkedList;

import org.apache.kylin.common.persistence.RootPersistentEntity;

import com.fasterxml.jackson.annotation.JsonAutoDetect;
import com.fasterxml.jackson.annotation.JsonProperty;

import lombok.Data;

@JsonAutoDetect(fieldVisibility = JsonAutoDetect.Visibility.NONE, getterVisibility = JsonAutoDetect.Visibility.NONE, isGetterVisibility = JsonAutoDetect.Visibility.NONE, setterVisibility = JsonAutoDetect.Visibility.NONE)
@Data
public class KafkaCluster extends RootPersistentEntity implements Serializable {
    public static final String RESOURCE_NAME = "kafka";

    @JsonProperty("history_kafka_brokers")
    private LinkedList<String> historyKafkaBrokers = new LinkedList<>();

    public String getIdentity() {
        return resourceName();
    }

    @Override
    public String resourceName() {
        return RESOURCE_NAME;
    }

    @Override
    public String getResourcePath() {
        return CLUSTER_RESOURCE_ROOT;
    }

}

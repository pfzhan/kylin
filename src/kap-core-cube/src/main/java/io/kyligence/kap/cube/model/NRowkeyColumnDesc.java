package io.kyligence.kap.cube.model;

import java.io.Serializable;

import com.fasterxml.jackson.annotation.JsonAutoDetect;
import com.fasterxml.jackson.annotation.JsonProperty;

import io.kyligence.kap.common.obf.IKeep;

@SuppressWarnings("serial")
@JsonAutoDetect(fieldVisibility = JsonAutoDetect.Visibility.NONE, getterVisibility = JsonAutoDetect.Visibility.NONE, isGetterVisibility = JsonAutoDetect.Visibility.NONE, setterVisibility = JsonAutoDetect.Visibility.NONE)
public class NRowkeyColumnDesc implements Serializable, IKeep {
    @JsonProperty("dimension_id")
    private int dimensionId;

    @JsonProperty("index")
    private String index;

    public NRowkeyColumnDesc() {
    }

    public int getDimensionId() {
        return dimensionId;
    }

    public void setDimensionId(int dimensionId) {
        this.dimensionId = dimensionId;
    }

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;

        NRowkeyColumnDesc that = (NRowkeyColumnDesc) o;

        if (dimensionId != that.dimensionId)
            return false;
        return index != null ? index.equals(that.index) : that.index == null;
    }

    @Override
    public int hashCode() {
        int result = dimensionId;
        result = 31 * result + (index != null ? index.hashCode() : 0);
        return result;
    }
}

package org.apache.kylin.query.plugin.runtime;


import org.apache.kylin.metadata.cube.model.NDataSegment;
import org.apache.spark.sql.Dataset;
import org.apache.spark.sql.Row;
import org.apache.spark.sql.SparkSession;
import org.apache.spark.sql.catalyst.plans.logical.LogicalPlan;

import java.util.List;

/**
 * Provides the ability to compute cuboid layout on-the-fly.
 */
public interface MppOnTheFlyProvider {

    /**
     * Compute layout on-the-fly for segments that don't have it.
     * <p/>
     * Return null if all segments have required layout.
     */
    LogicalPlan computeMissingLayout(List<NDataSegment> prunedSegments, long layoutId, SparkSession ss);
}
